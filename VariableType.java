package ch02_variable;

public class VariableType {
	// 변수의 타입(기본 타입)
	
	// 정수 타입
	byte byteVar = 1;
	short shorVar = 2;
	int intVar = 3;
	long longVar = 4l; //long은 끝에 l를 붙여준(대소문자를 구분할 필요는 없다)
	long longVal = 5L;
	
	char charVar = 5;
	char charVal = '짱';
	
	// 실수 타입
	double doubleVar = 3.14;
	float floatVar = 3.14f; //float은 끝에 f를 붙여준(대소문자를 구분할 필요는 없다)
	float floatVal = 3.14F;

	// 불리언 타입 (참/거짓, true/false)
	boolean boolVar = true;
	boolean boolVal = false;
	
	
	
	
	
	
	
	

	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
